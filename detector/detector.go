package detector

import (
	"fmt"
	"io"
)

var tree *trie

func init() {
	// Create a tree
	tree = &trie{
		root: &node{},
	}

	// fill tree
	for _, s := range sigs {
		tree.insert(s.Signature, s)
	}
}

// Detect detect input content type
func Detect(r io.Reader) (*Sig, error) {
	prev := tree.root
	var n *node
	for {
		b := make([]byte, 1)
		_, err := r.Read(b)
		if err != nil {
			if err == io.EOF {
				if prev != nil {
					return prev.value.(*Sig), nil
				}
				return nil, fmt.Errorf("can't detect type")
			}
			return nil, err
		}

		n = prev.getChild(b[0])
		if n != nil {
			prev = n
			continue
		}

		if prev != nil {
			return prev.value.(*Sig), nil
		}

		return nil, fmt.Errorf("undefined type: %v\n", prev.value)
	}
}

